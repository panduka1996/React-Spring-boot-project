package ie.ucd.rawana.simulatorapi.DTO;

public class ClockRegistry {

    private String name;

    private String uri;

    public ClockRegistry() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getUri() {
        return uri;
    }

    public void setUri(String uri) {
        this.uri = uri;
    }
}
